Pixels are used all the time within openHabbo, and you are able to collect them whilst you earn respect, complete quests and many other tasks throughout the openHabbo Hotel! You must remember, that Pixels can be used for furni!<br/><br/>So, if you feel as though you are a little short on coins, it's always worth checking your pixels! If you want to earn pixels, you can do it in more ways than one, they can eben be used by buying VIP, a nice little temptation that the administrators of openHabbo have put in for us!<br/><br/><br/><br/>
<b>So let's begin...</b><br/>
<i>There are a number of ways you can get Pixels!</i><br/><br/>
&bull; Completing achievements.<br/>
&bull; Completing quests!<br/>
&bull; Buying VIP.<br/>
&bull; Winning competitions.<br/>
&bull; Signing up to the hotel.<br/>
<br/>
These are just a few of the many ways that you can gain Pixels, and there are also a number of things you can use them for; you can buy effects and furni! The main way you can genuinely spend pixels is by going to the catalogue and selecting the 'pixel shop' from there on it's pretty self explanatory on what you need to do!<br/><br/>
<img src="../images/guides/pixels/1.png" align="right" />
This is the pixel shop on the openHabbo Hotel, by using the pixel shop you are able to purchase a number of pieces of furniture and effects ready for your every need, most effects and furnitutr are not considered, this is because they are openly available to every single member of the hotel; the only way these could become rare is if they are removed from the catalogue!<br/><br/>There are a lot of things you can gain from having effects and furniture from the pixel shop; you let people know that you are good at collecting pixels. Another point is, you are only allowed to use an effect for an hour, it then disappears and needs to be bought again! There's another reason for you to collect as many pixels as you can!<br/><br/><br/>
